public class Car {

    int x, y;

    Car(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void updateCarPosition() {
            x -= 10;
        if (x < -150) {
            x = 800;
        }

    }

}
